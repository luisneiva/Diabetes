        <h2 class="text-center">Praticar desporto com diabetes</h2>
        <p>1. Meça a glicemia antes de praticar exercício</p>
        <p>Se for inferior a 100 mg/dl tome um suplemento (uma peça de fruta, bebidas energéticas…). Se estiver entre 100 mg/dl – 250 mg/dl, pode praticar-se exercício físico sem problemas, mas cada pessoa é diferente e deve conhecer a sua janela de valores.</p>
        <p>2. Nunca faça exercício físico com valores superiores a 250 mg/dl</p>
        <p>Quando praticamos exercício, os músculos libertam glicose para a corrente sanguínea e o nível de açúcar no sangue aumenta, podendo correr o risco de cetoacidosis diabética, uma alteração muito grave que se não for tratada pode causar coma diabético.</p>
        <p>3. Avalie o nível de esforço e intensidade da actividade que vai realizar</p>
        <p>Se realiza uma actividade aeróbica, o seu nível de açúcar no sangue irá diminuir pouco a pouco. Se fizer uma actividade anaeróbica, os seus níveis de açúcar podem aumentar ou permanecerem elevados durante e depois da actividade.</p>
        <p>4. Faça exercício pelo menos duas horas depois de ter comido</p>
        <p>É importante adoptar esta medida para poder medir de forma mais rigorosa a glicemia.</p>
        <p>5. Ande prevenido com barritas</p>
        <p>Deve andar prevenido com hidratos de carbono como barritas</p><p>6. Proteja as zonas musculares mais activas
        <p>Se injectar insulina antes do exercício físico, não o deve fazer nas zonas onde os músculos estão mais implicados. Procure escolher uma zona muscular menos activa no exercício que vai realizar.
        <p>7. Controle os seus níveis de açúcar</p>
        <p>É importante que controle os seus níveis de açúcar no sangue durante o exercício.</p>
        <p>8. Esteja atento às hipoglicemias</p>
        <p>As hipoglicemias devido à prática de exercício físico podem surgir até 48 horas após o esforço. É importante saber isto para ajustar a quantidade que se injecta e a quantidade de hidratos de carbono que se toma.</p>
        <p>9. Mantenha-se hidratado durante o exercício</p>
        <p>Beba preferencialmente água.</p>
