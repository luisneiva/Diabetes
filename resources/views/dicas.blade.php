        <h2 class="text-center">Dieta para quem tem diabetes – O que comer?</h2>
        <p><b>Arroz, massa e pão integral</b></p>
        <p>Um cuidado especial para os diabéticos é a ingesta de carboidratos de maneira moderada. Neste caso, os que aportam estes alimentos são digeridos mais lentamente pelo organismo, o que termina libertando a glicose de maneira mais lenta.</p>
        <p>Os carboidratos integrais são os mais indicados porque contem maior quantidade de fibra.</p>
        <p><b>Carboidratos simples e açúcar</b></p>
        <p>Quando você é diagnosticado com diabetes a tendência é a de pensar que não se poderão comer mais doces. No entanto isso não é totalmente correto. Os doces podem ser consumidos, sempre e quando seja de maneira esporádica e seguindo uma série de cuidados. Se quer comer um docinho deve reduzir outros alimentos ricos em carboidratos para compensar. Se é dependente da insulina deve ter em conta que a dose deve ser aumentada também.</p>
        <p><b>Frutas</b></p>
        <p>Tome atenção ao consumo de frutas. Uma dieta para quem tem diabetes deve conter estes alimentos sim, no entanto de maneira inteligente. Tenha em conta que são carboidratos simples, com alto nível de açúcar. É o alto teor de fibras e outros nutrientes que permitem que um diabético possa incluir em sua dieta este alimento. O aconselhado é consumir entre 3 a 4 porções diárias, optando sempre pelas que contêm menos calorias.</p>
        <p><b>Sucos</b></p>
        <p>Os sucos de frutas costumam ser uma armadilha para um diabético. Se normalmente são aconselhados, em uma dieta para quem sofre de diabetes, deve ser controlado. Um suco contem a ração diária de fruta permitida para um paciente, o que torna o seu consumo perigoso, já que pode elevar a glicemia e muito.</p>
        <p><b>Leite desnatado</b></p>
        <p>O leite é algo importante na dieta de um diabético. Prefira o leite desnatado e reduza a quantidade de gordura no organismo.</p>
        <p><b>Carne vermelha</b></p>
        <p>As carnes vermelhas são ricas em ferro e vitamina B12, pelo que o consumo é aconselhável sim, mas de maneira inteligente. Em uma dieta para quem tem diabetes evite as que possuem uma grande quantidade de gorduras saturadas, como por exemplo, o filé mignon, a picanha e o contra-filé. Não obstante, dê preferencia às carnes brancas.</p>
        <p><b>Peixe</b></p>
        <p>Altamente recomendável. Os melhores são os que têm altas quantidades de gordura “boa”, como as trutas, o salmão e as sardinha. Consuma sempre cozido ou assado.</p>
        <p><b>Legumes e verduras</b></p>
        <p>Abuse das verduras e legumes. São alimentos ricos em fibras, vitaminas e minerais antioxidantes, e que podem e devem ser consumidos à vontade. Opte por produtos da estação e prefira o alimento cru, cozido ou assado.</p>
        <p><b>Porções</b></p>
        <p>Cuidado com a quantidade. É aconselhável que faça três refeições diárias e que coma depois entre horas, evitando ficar mais de 3 horas sem comer. Pequenas quantidades, já que uma comida volumosa estragará todo o esforço que implica uma dieta para quem tem diabetes. Beba muita água, ainda que o aconselhável é antes e depois das refeições.</p>



